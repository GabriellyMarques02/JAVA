package Basico;

public class operadoresdecomparacao {

	public static void main(String[] args) {
		boolean resultado = 10==10; // igual
		resultado = 10!=1; //diferença
		resultado = 10>1; // maior (também usado com <)
		resultado = 10>=1; // maior ou igual
		resultado = 10<=1; //menor ou igual
		//lembrando que ta reescrevendo o valor de resultado, logo o que vai exibir é o ultimo teste.
		System.out.println(resultado);
		

	}

}
